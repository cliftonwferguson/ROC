package basics;

public class PrimativeTypes {
	/*
	 * A variable is a construct that is used
	 * to hold onto some information.
	 * 
	 * In Java, we can specify two types of variables 
	 */
  byte b = 127; //whole number 8 bits
  short s = 12345; //whole number that is 16 bits
  int i = 1_000_000; //whole number 32 bits
  long l = 1_000_000_000l;  //whole number 64 bits
  float f = 1_000_000;
  double d;
  boolean bool;
  char c;
}
